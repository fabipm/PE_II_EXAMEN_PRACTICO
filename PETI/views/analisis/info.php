    <h2 class="mb-4">5. ANÁLISIS INTERNO Y EXTERNO</h2>

    <p>Fijados los objetivos estratégicos se debe analizar las distintas estrategias para lograrlos. De esta forma, las estrategias son los caminos, vías o enfoques para alcanzar los objetivos. Responden a la pregunta <strong>¿cómo?</strong>.</p>

    <p>Para determinar la estrategia, podríamos basarnos en el conjunto de estrategias genéricas y específicas que diferentes profesionales proponen al respecto. Esta guía, lejos de rozar la teoría, propone llevar a cabo un análisis interno y externo de su empresa para obtener una matriz cruzada e identificar la estrategia más conveniente a llevar a cabo.</p>

    <p>Este análisis le permitirá detectar por un lado los factores de éxito (<strong>fortalezas</strong> y <strong>oportunidades</strong>), y por otro lado, las <strong>debilidades</strong> y <strong>amenazas</strong> que una empresa debe gestionar.</p>

    <img src="/PETI/assets/img/extras/image1.png" alt="Imagen de análisis" class="img-fluid mb-3">

    <hr>

    <h4>Oportunidades</h4>
    <p>Son aquellos aspectos que pueden presentar una posibilidad para mejorar la rentabilidad de la empresa, aumentar la cifra de negocio y fortalecer la ventaja competitiva.</p>
    <p><strong>Ejemplos:</strong> Fuerte crecimiento, desarrollo de la externalización, nuevas tecnologías, seguridad de la distribución, atender a grupos adicionales de clientes, crecimiento rápido del mercado, etc.</p>

    <h4>Amenazas</h4>
    <p>Son fuerzas y presiones del mercado o entorno que pueden impedir y dificultar el crecimiento de la empresa, la ejecución de la estrategia, reducir su eficacia o incrementar los riesgos en relación con el entorno y el sector de actividad.</p>
    <p><strong>Ejemplos:</strong> Competencia en el mercado, aparición de nuevos competidores, reglamentación, monopolio en una materia prima, cambios en las necesidades de los consumidores, creciente poder de negociación de clientes y/o proveedores, etc.</p>

    <h4>Fortalezas</h4>
    <p>Son capacidades, recursos, posiciones alcanzadas, ventajas competitivas que posee la empresa y que le ayudarán a aprovechar las oportunidades del mercado.</p>
    <p><strong>Ejemplos:</strong> Buena implantación en el territorio, notoriedad de la marca, capacidad de innovación, recursos financieros adecuados, ventajas en costes, líder en el mercado, buena imagen ante los consumidores, etc.</p>

    <h4>Debilidades</h4>
    <p>Son todos aquellos aspectos que limitan o reducen la capacidad de desarrollo de la empresa. Constituyen dificultades para la organización y deben, por tanto, ser controladas y superadas.</p>
    <p><strong>Ejemplos:</strong> Precios elevados, productos en el final de su ciclo de vida, deficiente control de los riesgos, recursos humanos poco cualificados, débil imagen en el mercado, red de distribución débil, falta de dirección estratégica clara, etc.</p>

    <hr>

